package sy.common.util;

public class ByteManipulationException extends Exception{
    public ByteManipulationException() {
    }

    public ByteManipulationException(String message, Throwable cause) {
        super(message, cause);
    }

    public ByteManipulationException(String message) {
        super(message);
    }

    public ByteManipulationException(Throwable cause) {
        super(cause);
    }
}
